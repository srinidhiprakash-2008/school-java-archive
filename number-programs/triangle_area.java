//THIS PROGRAM IS TO FIND THE AREA OF A TRIANGLE//
import java.io.*;
class TRIAREA
{
    public void main( double breadth, double height)
    {
        double area;
        area=0.5*breadth * height;
        System.out.println("THE BREADTH OF THE TRIANGLE IS"+ breadth);
        System.out.println("THE HEIGHT OF THE TRIANGLE IS "+ height);
        System.out.println("THE AREA OF THE TRIANGLE IS"+ area);
    }
}