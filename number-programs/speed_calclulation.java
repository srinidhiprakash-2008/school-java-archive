//THIS PROGRAM IS TO FIND THE SPEED OF A BODY BY USING THE FORMULA//
import java.io.*;
class SPEED
{
    public void main(int distance, int time)
    {
        int speed;
        speed = distance/time;
        System.out.println("THE DISTANCE COVERED  BY THE BODY IS "+ distance);
        System.out.println("THE TIME TAKEN BY THE BODY IS "+ time);
        System.out.println("THE SPEED OF THE BODY IS" + speed);
    }
}